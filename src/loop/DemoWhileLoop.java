package loop;

public class DemoWhileLoop {

    public static void main(String[] args) {

        int i=1;

        while (i<=5){

            System.out.println("inside while loop.."+i);
            i++;
        }

        while(i>0){

            System.out.println("Decremental while loop..."+i);
            i--;
        }


    }
}
