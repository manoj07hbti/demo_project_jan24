package variables;

public class VariableOperatorClassRevision {

    public static void main(String[] args) {

        //Post Increment Operator Variable++ = VARIABLE+1

        int Rajeev=10;
        System.out.println("Printing Rajeev Before ++ operator :"+Rajeev);
        Rajeev++; // post increment
        System.out.println("Printing Rajeev after ++ operator :"+Rajeev);

        int Kapoor=11;
        Kapoor--;
        System.out.println("Printing Kapoor :"+Kapoor);



    }


}
