package variables;

public class VariableManipulation {

    public static void main(String[] args) {

        // Addition of two number
        int a=4;
        int b=7;

        int sum=a+b;

        System.out.println("Sum os a and b is :"+sum);

        int sub=a-b;
        System.out.println("Subtraction  os a and b is :"+sub);

        int multi=a*b;
        System.out.println("Printing multiplication "+multi);

        double divide=(double) a/b; // 0.57

        System.out.println("Division is: "+divide);


    }
}
