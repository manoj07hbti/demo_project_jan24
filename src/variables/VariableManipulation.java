package variables;

public class VariableManipulation {

    public static void main(String[] args) {

        // Addition of two number
        int a=5;
        int b=9;

        int sum=a+b;

        System.out.println("Sub of a and b is :"+sum);

        int sub=a-b;
        System.out.println("Subtraction of a and b is :"+sub);

        int multi=a*b;
        System.out.println("Multiplication of a and b is :"+multi);

        double divide=(double)a/b;
        System.out.println("divide of a and b is :"+divide);

    }



}
