package variables;

public class VariableIncrementOperator {
    public static void main(String[] args) {

        // Post increment Operator variable++ =variable+1

        int Pankaj=7;
        System.out.println("Printing Pankaj before ++ Operator : "+Pankaj);

        Pankaj++;  // Post increment

        int Singh=9;
        Singh--;
        System.out.println("Printing Singh :"+Singh);
    }
}
