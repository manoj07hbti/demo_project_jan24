package variables;

public class VariableManipulationSquareCube {

    public static void main(String[] args) {

        // if A= 6 // what is the square value of A;
        int A=6;
        int square=A*A;
        System.out.println("square of A is : "+square);

        // if B= 9 // what is the cube value of B;
        int B=9;
        int cube=B*B*B;
        System.out.println("cube of B is : "+cube);




    }

}
