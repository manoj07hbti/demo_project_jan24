package Loops.For_Loops;

public class Printing_Table {
    public static void main(String[] args) {


        for (int i =1;i<=10;i++){
            int table = i * 5;
            System.out.println(i+ " X " + "5 = "+table);
        }

        System.out.println("=====================================");

        for (int i =1;i<=10;i++){
            int table2 = i * 10;
            System.out.println(i+ " X " + "10 = "+table2);
        }
    }
}
