package my_conditional_statement;

public class DemoIf {
    public static void main(String[] args) {
        int number=60;
        if(number>=60){
            System.out.println("Student got first division-"+number);
        }
        int number1=33;
        if(number1<=33){
            System.out.println("Student will not cleared the exam-"+number1);
        }
        String country="India";
        if(country=="India"){
            System.out.println("you are Indian-"+country);
        }
        String countryname="Uk";
        if(countryname!="Indian"){
            System.out.println("you are not a Indian-"+countryname);
        }
    }

}
