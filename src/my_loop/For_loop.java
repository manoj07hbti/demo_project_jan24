package my_loop;

public class For_loop {
    public static void main(String[] args) {
        //table of 5 using for loop
        int number = 5;
        for (int i = 1; i <= 10; i++) {
            System.out.println("5 x " + i + " = " + number * i);
        }
        //table of 10 using for loop
        int num = 10;
        for (int i = 1; i <= 10; i++) {
            System.out.println("10 x " + i + " = " + num * i);
        }
    }
}
